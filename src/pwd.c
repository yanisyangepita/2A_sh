#include "../include/pwd.h"


/* ------------------------------------------------------------------------- */
/* Function     : pwd                                                        */
/*                                                                           */
/* Description  : print the current work directory                           */
/* ------------------------------------------------------------------------- */

void pwd()
{
    char wd[1000];
    printf("%s", getcwd(wd, sizeof(wd)));
}


/* ------------------------------------------------------------------------- */
/* Function     : get_wd                                                     */
/*                                                                           */
/* Description  : return the current working directory                       */
/* ------------------------------------------------------------------------- */
char* get_wd()
{
    char* wd = malloc(sizeof(char) * 1000);
    getcwd(wd, sizeof(wd));

    return wd;
}
